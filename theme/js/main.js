var disqus_comments = function(disqus) {
  
  //Set global variables
  window.disqus_shortname = disqus.shortname;
  window.disqus_identifier = disqus.identifier;
  window.disqus_url = disqus.url;
  window.disqus_developer = disqus.developer;

  //Anon function to add comment count
  (function () {
    var s = document.createElement('script');
    s.async = true;
    s.type = 'text/javascript';
    s.src = '//' + disqus.shortname + '.disqus.com/count.js?';
    (document.getElementsByTagName('HEAD')[0] || document.getElementsByTagName('BODY')[0]).appendChild(s);
  }());

  //Attach load comments function to global namespace
  window.load_comments = function() {
    var dsq = document.createElement('script');
    dsq.type = 'text/javascript'; dsq.async = true;
    dsq.src = '//' + disqus.shortname + '.disqus.com/embed.js';
    (document.getElementsByTagName('head')[0] || document.getElementsByTagName('body')[0]).appendChild(dsq);
  }
}
