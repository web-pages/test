(function() {
	var setScreenHeight = function() {
		var innerHeight = window.innerHeight || document.documentElement.clientHeight || document.body.clientHeight,
			styleHeight = styleHeight = innerHeight.toString() + 'px',
			styleHeightHalf = (innerHeight/2.0).toString() + 'px',
			elements,
			i, j;

		for (j = 0; j < 2; j++) {
			if (j == 0) {
				elements = document.getElementsByClassName('screen-height');
			} else {
				elements = document.getElementsByClassName('screen-height-responsive');
			}

			for (i = 0; i < elements.length; i++) {
				if (j === 0 || innerHeight < 768) {
					elements[i].style.minHeight = styleHeight;
				} else {
					elements[i].style.minHeight = styleHeightHalf;
				}
			}
		}
	}

	setScreenHeight();

	var timeout,
		setScreenHeightOnResize = function() {
			if (timeout)
				clearTimeout(timeout);
			
			//Use a timeout so that event queue does not become saturated with these requests
			timeout = setTimeout(setScreenHeight, 200); //200 milliseconds after resizing, reset the affixes	
		}

	if (window.addEventListener)
		window.addEventListener("resize", setScreenHeightOnResize);
	else if (window.attachEvent) //For older browsers
		window.attachEvent("onresize", setScreenHeightOnResize);
})();
