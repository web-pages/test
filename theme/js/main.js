(function($) {
	$(function() {
		var offsetTop = ($("#navbar-top").length && typeof noscrollanimateoffset === 'undefined') ? $("#navbar-top").outerHeight(true) : 0;

		//Make all internal anchors animate their page scroll
		$('a[href*=#]:not([href=#])').on('click', function(event) {
			if (location.pathname.replace(/^\//,'') === this.pathname.replace(/^\//,'') && location.hostname === this.hostname) {
				var target = $(this.hash);
				target = target.length ? target : $('[name=' + this.hash.slice(1) +']');

				if (target.length) {
					paddingmargin = (typeof noscrollanimateoffset === 'undefined')
						? (parseInt(target.css('padding-top')) + parseInt(target.css('margin-top')))
						: 0;
						
					$('html, body').animate({
						scrollTop: target.offset().top - offsetTop + paddingmargin
					}, 1000, 'jswing');

					event.preventDefault();
				}
			}
		});

		//Make all internal links fade between transitions (although we ignore the #share section)
		$('a[href]:not(#share a)').on('click', function(event) {
			if (this.hostname === location.hostname && this.pathname === location.pathname)
				return; //this is something on the same page, and it is (probably) handled by a scroll animation above

			if ($('body').is(':visible')) {
				var href = $(this).attr('href');
				$('body').fadeOut('slow', function() {
					window.location.href = href; //go to actual location
				});
				event.preventDefault();
			}
		});
	});
})(jQuery);