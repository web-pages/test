(function($) {
	$(function() {

		//Global vars
		var doctrinaAffix,
			parent,
			parentSibling,
			offsetTop,
			offsetTopPadding,
			offsetBottom,
			offsetBottomPadding;

		//Resets affix (needed when window is resized)
		var affixReset = function(elements) {
			//ToDo - Reset to remember states if affixes are in the middle of the screen
			$(window).off('.affix');

			$.each(elements, function(idx, val) {
				if (val.length)
					val.removeData('bs.affix').removeClass('affix affix-top affix-bottom');
			});
		}

		//Initializes affix elements. NOTE: Affix doms are hardcoded in elements attribute
		var affixInit = function(elements) {
			var content = $("#content"); //hard coded

			if (!content.length)
				return false;

			//Reset needed in case of screen resize
			affixReset(elements);

			parent = content.closest("section");
			parentSibling = parent.nextAll("section").first().offset();
			offsetTop = 0;
			offsetTopPadding =  $("#navbar-top").outerHeight(true);
			offsetBottom = $('body').height() - parentSibling.top;
			offsetBottomPadding = 0;

			//Find first element that is defined to set top offset
			$.each(elements, function(idx, val) {
				if (val.length !== 0) {
					offsetTop = val.offset().top;
					return false;
				}
			});

			return true;
		}

		//Get height of TOC when active class is applied to a section
		var getTOCHeight = function(tocAffix, activeSection) {
			if (!activeSection.length) return 0

			var classPresent = activeSection.hasClass('active'),
				height = 0;

			if (!classPresent) {
				activeSection.addClass('active');
			}
			height = tocAffix.outerHeight(true);
			if (!classPresent) {
				activeSection.removeClass('active');
			}
			
			return height;
		}

		//Makes extra padding at the bottom for the content element so 
		//that when scrolling to the bottom, the TOC menu will always
		//be visible
		var TOCExtraPaddingForContentParent = function(tocAffix) {
			var lastTOCItem = $('div.toc').children('ul.nav').find('> li').last(),
				prevTOCItem = lastTOCItem.prev(),
				maxHeight = Math.max(getTOCHeight(tocAffix, prevTOCItem), getTOCHeight(tocAffix, lastTOCItem));

			//The last child could be a subsection, in which
			//case, the needed padding must be calculated from it
			if (lastTOCItem.children.length) {
					var lastSection = lastTOCItem.children('ul.nav').find('> li').last();
					if (lastSection.length)
						lastTOCItem = lastSection;
			}

			var lastSectionLink = $('a', lastTOCItem).attr('href'),
				extraPadding = Math.max(0, maxHeight - (parentSibling.top - $(lastSectionLink).offset().top));

			tocAffix.css({'min-height': maxHeight.toString()+'px'});

			if (extraPadding && extraPadding > 0) {
				parent.css({'padding-bottom' : extraPadding.toString() + 'px'});
			}
		}

		//Customization logic for an element
		var customize = function(el) {
			switch(el.attr('id')) {

				//Set the extra padding for the content so that TOC is never cut off
				case "toc-affix":
					TOCExtraPaddingForContentParent(el);
					return true;

				//Shares for devices < 992 are not affixed
				case "share":
					return ($(window).width() >= 992);
			}
		}

		//Set Affixes
		var affixSettings = function(el) {
			if (!el.length || !el || !el.is(':visible'))
				return;

			if (!customize(el))
				return;
				
			//Set element to be affixed
			el.affix({
				offset: {
					top: offsetTop - offsetTopPadding,
					bottom: offsetBottom - offsetBottomPadding
				}
			});

			//Event listening just before element becomes affixed
			el.on('affix.bs.affix', function() {
				//When position becomes fixed, both width
				//and top css attributes need to be set
				el.css({'top':offsetTopPadding, 'width':el.width(), 'position':'fixed'});
			});

			//Event listening just before element becomes affixed top
			el.on('affix-top.bs.affix', function() {
				el.css({'top':'auto', 'width':'auto', 'position':'static'});
			});
		}

		var setTBSAffix = function() {
			var elements = [$("#toc-affix"), $("#share")];
		
			if (!affixInit(elements)) 
				return;

			$.each(elements, function(idx, val) {
				affixSettings(val);
			});
		}

		setTBSAffix();

		//When window resizes, we need to reset the affix
		var timeout;
		$(window).on('resize', function() {
			if (timeout)
				clearTimeout(timeout);

			//Use a timeout so that event queue does not become saturated with these requests
			timeout = setTimeout(setTBSAffix, 500); //500 milliseconds after resizing, reset the affixes
		})
	});
})(jQuery);
//JavaScript to make navbar auto-collapse on mobile

(function($) {
        $(function() {
            var collapseAll = function(elements, originator) {
                var numberOfEl = elements.length;

                elements.on('hidden.bs.collapse', function() {
                    numberOfEl--;

                    if (numberOfEl === 0) {
                        if (typeof originator !== "undefined")
                            originator.trigger('click');
                    }
                });

                elements.each(function(idx, val){
                    $(val).collapse('hide');
                });
            }

            $('button.navbar-toggle').on('click', function(event) {
                var elements = $('.navbar-collapse[aria-expanded=true]'),
                    originator = $(this),
                    originatorDataTarget = $(originator.attr('data-target'))[0]; //take first element

                //Remove the originator's data target.
                elements.each(function(idx, val){
                    if (val === originatorDataTarget)
                        elements.splice(idx, 1);
                });

                if (elements.length) {
                    collapseAll(elements, originator);
                    
                    event.preventDefault();
                    return false;
                }
            });

            $('#navigation-menu, #navigation-menu-toc').on('click', function() {
                collapseAll($(this));
            });
        });
})(jQuery);

